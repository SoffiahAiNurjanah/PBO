    private void btn_ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ubahActionPerformed
        // TODO add your handling code here:
        if(tidak_kosong()){
            try{
                stat= con.createStatement();
                String sql= "UPDATE penjualan SET No_Pemesanan='"+txt_no.getText() +"', Nama='"+txt_nama.getText() +"', Kode_Kereta='"+cmbkode.getSelectedItem() +"', Jenis_Kereta='"+cmbjenis.getSelectedItem() +"', Jurusan='"+cb_jurusan.getSelectedItem() +"', Jumlah_Tiket='"+txt_jml.getText() +"', Harga='"+txtharga.getText() +"', Total_Bayar='"+txt_total.getText() +"'";
                stat.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Berhasil Diubah");
                tampildata();
            } catch(SQLException e){
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Terjadi Kesalahan");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Harap Lengkapi Data", "Kesalahan", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_ubahActionPerformed

