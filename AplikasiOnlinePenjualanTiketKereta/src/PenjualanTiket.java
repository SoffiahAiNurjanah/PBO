    public void tampildata(){
      try{
        Connection con = koneksi.getKoneksi();
        Statement stat = con.createStatement();
        ResultSet rs = stat.executeQuery("SELECT * from penjualan");
        DefaultTableModel model = (DefaultTableModel) tb_penjualan.getModel();
        tabmodel.setRowCount(0);
        
        while(rs.next()){
            String No_Pemesanan = rs.getString("No_Pemesanan");
            String Nama = rs.getString("Nama");
            String Kode_Kereta = rs.getString("Kode_Kereta");
            String Jenis_Kereta = rs.getString("Jenis_Kereta");
            String Jurusan = rs.getString("Jurusan");
            String Jumlah_Tiket = rs.getString("Jumlah_Tiket");
            String Harga = rs.getString("Harga");
            String Total_Bayar = rs.getString("Total_Bayar");
            model.addRow(new Object[]{No_Pemesanan,Nama,Kode_Kereta,Jenis_Kereta,Jurusan,Jumlah_Tiket,Harga,Total_Bayar});
        }
        
        }catch(SQLException e){
         System.out.println(e.getMessage());
          JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat terhubung ke database");
        }
    }
    
